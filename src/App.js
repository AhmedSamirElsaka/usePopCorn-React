function MoviesList({ movies, isWatched }) {
  return (
    <ul className="list">
      <ul className="list">
        {movies?.map((movie) => (
          <MovieItem key={movie.imdbID} movie={movie} isWatched={isWatched} />
        ))}
      </ul>
    </ul>
  );
}

function MovieItem({ movie, isWatched }) {
  return (
    <li key={movie.imdbID}>
      <img src={movie.Poster} alt={`${movie.Title} poster`} />
      <h3>{movie.Title}</h3>
      {!isWatched ? (
        <UnWatchedMoviesDetails movie={movie} />
      ) : (
        <WatchedMovieDetails movie={movie} />
      )}
    </li>
  );
}
